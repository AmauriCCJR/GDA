      <div class="col-lg-8 col-12 mx-auto">
        <h1 class="gda_title_home text-center">Política de Privacidade</h1>
        <p class="text-center text-muted mb-5">Última atualização: 23 de março de 2026</p>

        <div class="gda_legal_box">
          <p>Esta Política descreve como o Sistema de Gestão de Processos Aduaneiros (GDA) coleta, usa, armazena e protege suas informações pessoais, em conformidade com a Lei Geral de Proteção de Dados (LGPD - Lei nº 13.709/2018).</p>

          <h2 class="gda_legal_heading">1. Informações que Coletamos</h2>

          <h3 class="gda_legal_subheading">1.1 Dados Pessoais</h3>
          <p>Coletamos as seguintes categorias de dados pessoais:</p>
          <ul>
            <li><strong>Dados de Identificação:</strong> Nome completo, CPF, RG, data de nascimento, nacionalidade</li>
            <li><strong>Dados de Contato:</strong> E-mail, telefone, endereço residencial e comercial</li>
            <li><strong>Dados Empresariais:</strong> Razão social, CNPJ, inscrição estadual, atividade econômica</li>
            <li><strong>Dados de Acesso:</strong> Login, senha criptografada, endereço IP, histórico de acessos</li>
            <li><strong>Dados Financeiros:</strong> Informações bancárias para pagamento de taxas, histórico de transações</li>
            <li><strong>Dados de Navegação:</strong> Páginas acessadas, tempo de permanência, dispositivo utilizado</li>
          </ul>

          <h3 class="gda_legal_subheading">1.2 Dados sobre Operações Comerciais</h3>
          <p>Durante o uso do sistema, também coletamos:</p>
          <ul>
            <li>Informações sobre mercadorias (descrição, classificação fiscal, valor, origem)</li>
            <li>Dados de transporte e logística</li>
            <li>Documentos fiscais e comerciais</li>
            <li>Certificados e licenças</li>
          </ul>

          <h2 class="gda_legal_heading">2. Como Coletamos seus Dados</h2>
          <p>Os dados são coletados através de:</p>
          <ul>
            <li>Cadastro direto: Informações fornecidas por você durante o registro ou atualização de perfil</li>
            <li>Uso do sistema: Dados gerados automaticamente durante a utilização da plataforma</li>
            <li>Documentos enviados: Arquivos e formulários submetidos através do sistema</li>
            <li>Cookies: Pequenos arquivos armazenados em seu navegador para melhorar a experiência</li>
            <li>Integrações: Dados recebidos de outros sistemas governamentais quando autorizado</li>
          </ul>

          <h2 class="gda_legal_heading">3. Finalidades do Tratamento</h2>
          <p>Utilizamos seus dados pessoais para as seguintes finalidades:</p>
          <ul>
            <li>Processar declarações e solicitações aduaneiras – Cumprimento de obrigação legal (Art. 7º, II)</li>
            <li>Gerenciar sua conta no sistema – Execução de contrato (Art. 7º, V)</li>
            <li>Comunicar sobre o status de processos – Execução de contrato (Art. 7º, V)</li>
            <li>Cumprir obrigações fiscais e regulatórias – Cumprimento de obrigação legal (Art. 7º, II)</li>
            <li>Prevenir fraudes e garantir segurança – Legítimo interesse (Art. 7º, IX)</li>
            <li>Melhorar a experiência do usuário – Legítimo interesse (Art. 7º, IX)</li>
            <li>Enviar notificações e comunicados – Consentimento (Art. 7º, I)</li>
          </ul>

          <h2 class="gda_legal_heading">4. Compartilhamento de Dados</h2>
          <p>Seus dados podem ser compartilhados com:</p>
          <ul>
            <li><strong>Receita Federal do Brasil:</strong> Para processamento de declarações e fiscalização</li>
            <li><strong>Outros órgãos governamentais:</strong> Quando exigido por lei ou regulamentação</li>
            <li><strong>Instituições financeiras:</strong> Para processamento de pagamentos de taxas</li>
            <li><strong>Prestadores de serviço:</strong> Empresas que nos auxiliam na operação do sistema (sob contrato de confidencialidade)</li>
            <li><strong>Autoridades competentes:</strong> Mediante ordem judicial ou requisição legal</li>
          </ul>

          <div class="gda_legal_alert">
            <strong>Importante:</strong> Nunca vendemos, alugamos ou compartilhamos seus dados pessoais para fins comerciais ou publicitários.
          </div>

          <h2 class="gda_legal_heading">5. Armazenamento e Segurança</h2>

          <h3 class="gda_legal_subheading">5.1 Localização dos Dados</h3>
          <p>Seus dados são armazenados em servidores governamentais localizados no território brasileiro, em conformidade com a legislação vigente.</p>

          <h3 class="gda_legal_subheading">5.2 Medidas de Segurança</h3>
          <p>Implementamos rigorosas medidas de segurança, incluindo:</p>
          <ul>
            <li>Criptografia de dados em trânsito (SSL/TLS) e em repouso</li>
            <li>Controles de acesso baseados em função e autenticação multifator</li>
            <li>Monitoramento contínuo de segurança e detecção de ameaças</li>
            <li>Backups regulares e planos de recuperação de desastres</li>
            <li>Auditorias de segurança periódicas</li>
          </ul>

          <h3 class="gda_legal_subheading">5.3 Período de Retenção</h3>
          <p>Mantemos seus dados pelo tempo necessário para:</p>
          <ul>
            <li>Cumprir as finalidades descritas nesta política</li>
            <li>Atender obrigações legais e regulatórias (mínimo de 5 anos após o encerramento da conta)</li>
            <li>Exercer direitos em processos judiciais, administrativos ou arbitrais</li>
          </ul>

          <h2 class="gda_legal_heading">6. Seus Direitos</h2>
          <p>De acordo com a LGPD, você tem os seguintes direitos:</p>
          <ul>
            <li><strong>Confirmação e Acesso:</strong> Confirmar se tratamos seus dados e solicitar uma cópia</li>
            <li><strong>Correção:</strong> Solicitar a correção de dados incompletos, inexatos ou desatualizados</li>
            <li><strong>Anonimização ou Bloqueio:</strong> Solicitar a anonimização ou bloqueio de dados desnecessários</li>
            <li><strong>Eliminação:</strong> Solicitar a exclusão de dados tratados com base no consentimento, nas hipóteses legais</li>
            <li><strong>Portabilidade:</strong> Solicitar a portabilidade de seus dados a outro fornecedor</li>
            <li><strong>Informação sobre Compartilhamento:</strong> Saber com quem compartilhamos seus dados</li>
            <li><strong>Revogação do Consentimento:</strong> Revogar o consentimento dado anteriormente</li>
            <li><strong>Oposição:</strong> Opor-se ao tratamento realizado com base em legítimo interesse</li>
          </ul>
          <p>Para exercer qualquer um desses direitos, você pode acessar as configurações de privacidade em sua conta ou entrar em contato com nosso Departamento de Proteção de Dados (DPD) através do e-mail <a href="mailto:dpd@gda.gov.br" class="gda_legal_link">dpd@gda.gov.br</a>.</p>

          <h2 class="gda_legal_heading">7. Cookies e Tecnologias Similares</h2>
          <p>Utilizamos cookies e tecnologias similares para:</p>
          <ul>
            <li>Manter sua sessão ativa e segura</li>
            <li>Lembrar suas preferências</li>
            <li>Analisar o uso do sistema e melhorar funcionalidades</li>
            <li>Detectar e prevenir fraudes</li>
          </ul>
          <p>Você pode gerenciar suas preferências de cookies nas configurações do navegador, mas isso pode afetar algumas funcionalidades do sistema.</p>

          <h2 class="gda_legal_heading">8. Transferência Internacional de Dados</h2>
          <p>O GDA não realiza transferência internacional de dados pessoais. Todos os dados são processados e armazenados exclusivamente no Brasil.</p>

          <h2 class="gda_legal_heading">9. Menores de Idade</h2>
          <p>O GDA não coleta intencionalmente dados de menores de 18 anos. Caso tome conhecimento de que coletamos dados de menores sem o consentimento adequado, tomaremos medidas para excluir tais informações.</p>

          <h2 class="gda_legal_heading">10. Alterações nesta Política</h2>
          <p>Podemos atualizar esta Política de Privacidade periodicamente. Notificaremos você sobre mudanças significativas através de:</p>
          <ul>
            <li>Aviso destacado no sistema</li>
            <li>E-mail para o endereço cadastrado</li>
            <li>Notificação no login</li>
          </ul>
          <p>Recomendamos revisar esta política regularmente para manter-se informado sobre como protegemos seus dados.</p>

          <h2 class="gda_legal_heading">11. Encarregado de Proteção de Dados (DPO)</h2>
          <p>Nosso Encarregado de Proteção de Dados está disponível para esclarecer dúvidas sobre esta política e sobre o tratamento de seus dados:</p>
          <ul>
            <li>Nome: João Noverno</li>
            <li>E-mail: <a href="mailto:dpo@gda.gov.br" class="gda_legal_link">dpo@gda.gov.br</a></li>
            <li>Telefone: 0800 123 1234 opção 3</li>
            <li>Endereço: Avenida Brigadeiro Faria Lima, 3243 – Torre B, 12º andar</li>
          </ul>

          <h2 class="gda_legal_heading">12. Autoridade Nacional de Proteção de Dados (ANPD)</h2>
          <p>Caso suas dúvidas ou solicitações não sejam respondidas satisfatoriamente, você tem o direito de apresentar uma reclamação à Autoridade Nacional de Proteção de Dados (ANPD):</p>
          <ul>
            <li>Website: <a href="https://www.gov.br/anpd" target="_blank" class="gda_legal_link">www.gov.br/anpd</a></li>
            <li>Canal de atendimento: Disponível no website da ANPD</li>
          </ul>

          <div class="gda_legal_alert">
            Ao se cadastrar no GDA, você declara ter lido e compreendido esta Política de Privacidade e concorda com o tratamento de seus dados pessoais conforme descrito acima.
